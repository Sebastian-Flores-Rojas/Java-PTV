    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();

        String nombre, idsashimi, preciosashimi;
        int id_venta, id_sashimi, precio, precio_final;

        nombre = (String)cbSashimi.getSelectedItem();
        id_venta = Integer.parseInt(txtFolio.getText());
        idsashimi = (String)cbIdSashimi.getSelectedItem();
        id_sashimi = Integer.parseInt(idsashimi);
        preciosashimi = (String)cbSashimiPrecio.getSelectedItem();
        precio = Integer.parseInt(preciosashimi);

        try
        {
            PreparedStatement pst = cn.prepareStatement("INSERT INTO venta_sashimi VALUES (?,?,?,?,?)");
            pst.setInt(2, id_venta);
            pst.setInt(3, id_sashimi);
            pst.setString(4, nombre);
            pst.setInt(5, precio);
            pst.executeUpdate();

            llenarTableSashimi(id_venta);
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

