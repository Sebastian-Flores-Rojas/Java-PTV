    public void llenarTableAcompañamiento(int folio){
        
        DefaultTableModel stock = new DefaultTableModel();
        
        stock.addColumn("ID");
        stock.addColumn("Nombre");
        stock.addColumn("Precio");
        
        String [] fila = new String[5];

        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            Statement st = cn.createStatement();
            ResultSet acompañamiento = st.executeQuery("SELECT * FROM venta_acompañamiento WHERE id_venta =" + folio);
                        
            while(acompañamiento.next()){
                
                System.out.println(acompañamiento.getString(3));
                
                fila[0] = acompañamiento.getString(1);
                //fila[1] = stock.getString(3) + " " + stock.getString(4);
                fila[1] = acompañamiento.getString(4);
                fila[2] = acompañamiento.getString(5);

                stock.addRow(fila);
            }
            
            tableAcompañamiento.setModel(stock);
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }
    
    public void llenarComboBoxAcompañamiento(){
        
        cbAcompañamiento.removeAllItems();
        cbIdAcompañamiento.removeAllItems();
        cbAcompañamientoPrecio.removeAllItems();
        DefaultComboBoxModel modeloAcompañamiento = new DefaultComboBoxModel();
        DefaultComboBoxModel modeloId = new DefaultComboBoxModel();
        DefaultComboBoxModel modeloPrecio = new DefaultComboBoxModel();
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            Statement st = cn.createStatement();
            ResultSet acompañamiento = st.executeQuery("SELECT * FROM acompañamiento");
            
            while(acompañamiento.next()){
                modeloAcompañamiento.addElement(acompañamiento.getString(2));
                modeloId.addElement(acompañamiento.getString(1));
                modeloPrecio.addElement(acompañamiento.getString(3));
            }
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
        cbAcompañamiento.setModel(modeloAcompañamiento);
        cbIdAcompañamiento.setModel(modeloId);
        cbAcompañamientoPrecio.setModel(modeloPrecio);
}
    
