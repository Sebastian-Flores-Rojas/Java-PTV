    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();

        String nombre, idacompañamiento, precioacompañamiento;
        int id_venta, id_acompañamiento, precio, precio_final;

        nombre = (String)cbAcompañamiento.getSelectedItem();
        id_venta = Integer.parseInt(txtFolio.getText());
        idacompañamiento= (String)cbIdAcompañamiento.getSelectedItem();
        id_acompañamiento = Integer.parseInt(idacompañamiento);
        precioacompañamiento = (String)cbAcompañamientoPrecio.getSelectedItem();
        precio = Integer.parseInt(precioacompañamiento);

        try
        {
            PreparedStatement pst = cn.prepareStatement("INSERT INTO venta_acompañamiento VALUES (?,?,?,?,?)");
            pst.setInt(2, id_venta);
            pst.setInt(3, id_acompañamiento);
            pst.setString(4, nombre);
            pst.setInt(5, precio);
            pst.executeUpdate();

            llenarTableAcompañamiento(id_venta);
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();

        int id, id_venta;

        id = Integer.parseInt(txtId.getText());
        id_venta = Integer.parseInt(txtFolio.getText());

        try
        {
            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Esta seguro que desea ELIMINAR este acompañamiento?");

            if (confirmacion == 0){

                PreparedStatement pst = cn.prepareStatement("DELETE FROM venta_acompañamiento WHERE id = ?");
                pst.setInt(1, id);
                pst.executeUpdate();

                //limpiar();
                llenarTableAcompañamiento(id_venta);
            }
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

