    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        String idsushi, preciosushi;
        
        idsushi = (String)cbIdSushi.getSelectedItem();
        preciosushi = (String)cbSushiPrecio.getSelectedItem();
        
        try
        {
            PreparedStatement pst = cn.prepareStatement("INSERT INTO venta_sushi VALUES (?,?,?,?,?)");
            pst.setInt(2, Integer.parseInt(txtFolio.getText()));
            pst.setInt(3, Integer.parseInt(idsushi));
            pst.setString(4, (String)cbSushi.getSelectedItem());
            pst.setInt(5, Integer.parseInt(preciosushi));
            pst.executeUpdate();
            llenarTableSushi(Integer.parseInt(txtFolio.getText()));
            pst.close();
            
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

