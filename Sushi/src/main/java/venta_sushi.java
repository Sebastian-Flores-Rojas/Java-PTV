    public void llenarTableSushi(int folio){
        
        DefaultTableModel stock = new DefaultTableModel();
        
        stock.addColumn("ID");
        stock.addColumn("Nombre");
        stock.addColumn("Precio");
        
        String [] fila = new String[5];

        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            Statement st = cn.createStatement();
            ResultSet sushi = st.executeQuery("SELECT * FROM venta_sushi WHERE id_venta =" + folio);
                        
            while(sushi.next()){
                
                fila[0] = sushi.getString(1);
                //fila[1] = stock.getString(3) + " " + stock.getString(4);
                fila[1] = sushi.getString(4);
                fila[2] = sushi.getString(5);

                stock.addRow(fila);
            }
            
            tableSushi.setModel(stock);
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }
    
    public void llenarComboBoxSushi(String tipo){
        
        cbSushi.removeAllItems();
        cbIdSushi.removeAllItems();
        cbSushiPrecio.removeAllItems();
        DefaultComboBoxModel modeloSushi = new DefaultComboBoxModel();
        DefaultComboBoxModel modeloId = new DefaultComboBoxModel();
        DefaultComboBoxModel modeloPrecio = new DefaultComboBoxModel();
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            Statement st = cn.createStatement();
            ResultSet sushi = st.executeQuery("SELECT * FROM sushi WHERE tipo = '" + tipo + "'");
            
            while(sushi.next()){
                modeloSushi.addElement(sushi.getString(2));
                modeloId.addElement(sushi.getString(1));
                modeloPrecio.addElement(sushi.getString(4));
            }
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
        cbSushiPrecio.setModel(modeloPrecio);
        cbSushi.setModel(modeloSushi);
        cbIdSushi.setModel(modeloId);
}
