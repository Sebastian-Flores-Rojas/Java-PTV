    public void ganancia(){
    Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            Statement st = cn.createStatement();
            ResultSet sushi = st.executeQuery("SELECT costo, precio_final, id FROM venta WHERE ganancia = 0");

            while(sushi.next()){
                 
                int ganancia = sushi.getInt(2)-sushi.getInt(1);
                int id = sushi.getInt(3);
        try
        {
            PreparedStatement pst = cn.prepareStatement("UPDATE venta SET ganancia = ? WHERE id = ?");
            pst.setDouble(1, ganancia);
            pst.setInt(2, id);
            pst.executeUpdate();
        }
        catch (SQLException x)
        {
            System.out.print(x.getMessage());
        }
                }
            }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }
    
