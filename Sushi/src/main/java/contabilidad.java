    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
        String fechaDesde = date.format(dcDesde.getDate());
        String fechaHasta = date.format(dcHasta.getDate());
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("Folio");
        modelo.addColumn("Pagado");
        modelo.addColumn("Cliente");
        modelo.addColumn("Fecha");
        modelo.addColumn("Hora Entrega");
        modelo.addColumn("Direccion");
        modelo.addColumn("Telefono");
        modelo.addColumn("Costo");
        modelo.addColumn("Ganancia");
        modelo.addColumn("Envio");
        modelo.addColumn("Precio");
        
        String [] fila = new String[11];

        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            Statement st = cn.createStatement();
            ResultSet venta = st.executeQuery("SELECT * FROM venta WHERE date(fecha)>= '" + fechaDesde + "' and date(fecha)<= '" + fechaHasta + "'");
                        
            while(venta.next()){
                //System.out.println(stock.getString(2) + " " + stock.getString(3) + " " + stock.getString(4));
                fila[0] = venta.getString(1);
                fila[1] = venta.getString(2);
                //fila[1] = stock.getString(3) + " " + stock.getString(4);
                fila[2] = venta.getString(3);
                fila[3] = venta.getString(4);
                fila[4] = venta.getString(5);
                fila[5] = venta.getString(6);
                fila[6] = venta.getString(7);
                fila[7] = venta.getString(8);
                fila[8] = venta.getString(9);
                fila[9] = venta.getString(10);
                fila[10] = venta.getString(11);
                modelo.addRow(fila);
                
                totalCostoFecha(fechaDesde, fechaHasta);
                totalGananciaFecha(fechaDesde, fechaHasta);
            }
            
            tableContabilidad.setModel(modelo);
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        String id, pago;
        
        pago = (String)cbPagado.getSelectedItem();
        id = txtId.getText();
        
        try
        {
            PreparedStatement pst = cn.prepareStatement("UPDATE venta SET pago = ? WHERE id = ?");
            pst.setString(1, pago);
            pst.setString(2, id);
            pst.executeUpdate();
            limpiar();
            llenarTabla();
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        } 
    }//GEN-LAST:event_btnActualizarActionPerformed

