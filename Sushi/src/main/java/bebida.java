    private void btnEliminarBebidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarBebidaActionPerformed
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();

        String nombre = txtNombreBebida.getText();

        try
        {
            int confirmacion = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro que desea ELIMINAR esta bebida?");

            if (confirmacion == 0){

                PreparedStatement pst = cn.prepareStatement("DELETE FROM bebida WHERE nombre = ?");
                pst.setString(1, nombre);
                pst.executeUpdate();
                limpiar();
                llenarTabla();
                llenarComboBoxBebida();
            }
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarBebidaActionPerformed

