    public void llenarTabla(){
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("Nombre");
        modelo.addColumn("Precio");
        
        String [] fila = new String[3];

        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            Statement st = cn.createStatement();
            ResultSet bebida = st.executeQuery("SELECT * FROM bebida");
                        
            while(bebida.next()){
                //System.out.println(stock.getString(2) + " " + stock.getString(3) + " " + stock.getString(4));
                //fila[1] = stock.getString(3) + " " + stock.getString(4);
                fila[0] = bebida.getString(2);
                fila[1] = bebida.getString(3);
                modelo.addRow(fila);
            }
            
            tableBebida.setModel(modelo);
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }
    
    public void llenarComboBoxBebida(){
        
        cbBebida.removeAllItems();
        cbIdBebida.removeAllItems();
        DefaultComboBoxModel modeloSalsa = new DefaultComboBoxModel();
        DefaultComboBoxModel modeloId = new DefaultComboBoxModel();
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            Statement st = cn.createStatement();
            ResultSet bebida = st.executeQuery("SELECT * FROM bebida");
            
            while(bebida.next()){
                modeloSalsa.addElement(bebida.getString(2));
                modeloId.addElement(bebida.getString(1));
            }
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
        cbBebida.setModel(modeloSalsa);
        cbIdBebida.setModel(modeloId);
}
    
