    private void btnCerrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCerrar1ActionPerformed
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();

        String nombre, idsashimi, preciosashimi, tipo;
        int id_venta, id_sashimi, id;

        nombre = (String)cbSashimi.getSelectedItem();
        id_venta = Integer.parseInt(txtFolio.getText());
        idsashimi = (String)cbIdSashimi.getSelectedItem();
        id_sashimi = Integer.parseInt(idsashimi);
        id = Integer.parseInt(txtFolio.getText());
        tipo = (String)cbTipoSashimi.getSelectedItem();

        try
        {
            PreparedStatement pst = cn.prepareStatement("INSERT INTO menu_sashimi VALUES (?,?,?,?,?)");
            pst.setInt(2, id_venta);
            pst.setInt(3, id_sashimi);
            pst.setString(4, tipo);
            pst.setString(5, nombre);
            pst.executeUpdate();

            llenarTableSashimi(id_venta);
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }//GEN-LAST:event_btnCerrar1ActionPerformed

    private void btnCerrar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCerrar2ActionPerformed
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();

        int id = Integer.parseInt(txtFolio.getText());
        String nombre = txtNombre.getText();

        try
        {
            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Esta seguro que desea ELIMINAR este sashimi?");

            if (confirmacion == 0){

                PreparedStatement pst = cn.prepareStatement("DELETE FROM menu_sashimi WHERE ( nombre = ? AND id_menu = ? )");
                pst.setString(1, nombre);
                pst.setInt(2, id);
                pst.executeUpdate();

                limpiar();
                llenarTableSashimi(id);
            }
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }//GEN-LAST:event_btnCerrar2ActionPerformed

