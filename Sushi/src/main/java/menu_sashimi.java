    public void llenarTableSashimi(int folio){
        
        DefaultTableModel stock = new DefaultTableModel();
        
        stock.addColumn("Tipo");
        stock.addColumn("Nombre");
        
        String [] fila = new String[5];

        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            Statement st = cn.createStatement();
            ResultSet sashimi = st.executeQuery("SELECT * FROM menu_sashimi WHERE id_menu =" + folio);
                        
            while(sashimi.next()){
                
                fila[0] = sashimi.getString(4);
                //fila[1] = stock.getString(3) + " " + stock.getString(4);
                fila[1] = sashimi.getString(5);

                stock.addRow(fila);
            }
            
            tableSashimi.setModel(stock);
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }
    
    public void llenarComboBoxSashimi(String tipo){
        
        cbSashimi.removeAllItems();
        cbIdSashimi.removeAllItems();
        DefaultComboBoxModel modeloSashimi = new DefaultComboBoxModel();
        DefaultComboBoxModel modeloId = new DefaultComboBoxModel();
        DefaultComboBoxModel modeloPrecio = new DefaultComboBoxModel();
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            System.out.println(tipo);
            Statement st = cn.createStatement();
            ResultSet sashimi = st.executeQuery("SELECT * FROM sashimi WHERE tipo = '" + tipo + "'");
            
            while(sashimi.next()){
                modeloSashimi.addElement(sashimi.getString(2));
                modeloId.addElement(sashimi.getString(1));
                modeloPrecio.addElement(sashimi.getString(4));
            }
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
        cbSashimi.setModel(modeloSashimi);
        cbIdSashimi.setModel(modeloId);
}
    
