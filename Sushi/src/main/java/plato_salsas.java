    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            int confirmacion = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro que desea ELIMINAR esta salsa?");

            if (confirmacion == 0){

                PreparedStatement pst = cn.prepareStatement("DELETE FROM palto_salsas WHERE id = ?");
                pst.setInt(1, Integer.parseInt(txtId.getText()));
                pst.executeUpdate();
                llenarTableSalsa(Integer.parseInt(txtIdPlato.getText()));
                limpiar();
            }
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

