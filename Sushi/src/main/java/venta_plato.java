    public void llenarTablePlato(int folio){
        
        DefaultTableModel stock = new DefaultTableModel();
        
        stock.addColumn("ID");
        stock.addColumn("Nombre");
        stock.addColumn("Precio");
        
        String [] fila = new String[5];

        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            Statement st = cn.createStatement();
            ResultSet sushi = st.executeQuery("SELECT * FROM venta_plato WHERE id_venta =" + folio);
                        
            while(sushi.next()){
                
                System.out.println(sushi.getString(3));
                
                fila[0] = sushi.getString(1);
                //fila[1] = stock.getString(3) + " " + stock.getString(4);
                fila[1] = sushi.getString(4);
                fila[2] = sushi.getString(5);

                stock.addRow(fila);
            }
            
            tablePlato.setModel(stock);
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
    }
    
    public void llenarComboBoxPlato(String tipo){
        
        cbPlato.removeAllItems();
        cbIdPlato.removeAllItems();
        cbPlatoPrecio.removeAllItems();
        DefaultComboBoxModel modeloPlato = new DefaultComboBoxModel();
        DefaultComboBoxModel modeloId = new DefaultComboBoxModel();
        DefaultComboBoxModel modeloPrecio = new DefaultComboBoxModel();
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
        try
        {
            System.out.println(tipo);
            Statement st = cn.createStatement();
            ResultSet plato = st.executeQuery("SELECT * FROM plato WHERE tipo = '" + tipo + "'");
            
            while(plato.next()){
                modeloPlato.addElement(plato.getString(2));
                modeloId.addElement(plato.getString(1));
                modeloPrecio.addElement(plato.getString(4));
            }
        }
        catch (SQLException ex)
        {
            System.out.print(ex.getMessage());
        }
        cbPlatoPrecio.setModel(modeloPrecio);
        cbPlato.setModel(modeloPlato);
        cbIdPlato.setModel(modeloId);
}
    
